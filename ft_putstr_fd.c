#include "ft_printf.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (fd == -1 || !s)
		return (0);
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	free (s);
	return (i);
}
